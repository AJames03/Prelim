#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#define p printf
#define sc scanf

// Betinol, Aron James L.
// BSIT-1F
// Find the Celsius and Fahrenheit
int main()
{
    float f, ans;

    p("Fahrenheit : ");
    sc("%f", &f);
    ans = (f-32)*5/9;

    p("Celsius : %.2f \n", ans);

    return 0;
}
